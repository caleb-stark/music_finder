const BATCH_SIZE = 50;
const sqlLines = [];
const failed = [];

for (const [rowId, , spotifyId] of rows) {
  try {
    const result = await spotifyApi.getTrack(spotifyId);
    const track = result.body;

    const title = escape(track.name);
    const artist = escape(track.artists.map(a => a.name).join(", "));
    const albumArt = track.album.images[0]?.url || "";

    sqlLines.push(
      `UPDATE movie_songs SET song_title='${title}', artist_name='${artist}', album_art_url='${albumArt}' WHERE id=${rowId};`
    );
    console.log(`  ✓ ${rowId}: ${track.name}`);
  } catch (err) {
    console.error(`  ✗ ${rowId} (${spotifyId}): ${err.statusCode}`);
    failed.push(rowId);
  }

  await delay(200);
}

